#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*a;
	int		i;

	a = (char *)malloc(sizeof(*s1) * ft_strlen(s1) + 1);
	i = 0;
	if (!a)
		return (NULL);
	while (s1[i])
	{
		a[i] = s1[i];
		i++;
	}
	a[i] = '\0';
	return (a);
}
